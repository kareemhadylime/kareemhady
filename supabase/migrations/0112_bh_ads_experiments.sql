create table if not exists public.ads_experiments (
  id              bigint generated by default as identity primary key,
  name            text not null,
  hypothesis      text,
  platform        text not null check (platform in ('meta','google','tiktok')),
  control_campaign_id  bigint references public.ads_campaigns(id) on delete set null,
  variant_campaign_ids bigint[] not null default '{}',
  split_pct       integer default 50 check (split_pct between 1 and 99),
  status          text not null default 'draft' check (status in ('draft','running','completed','aborted')),
  started_at      timestamptz,
  ended_at        timestamptz,
  winner_campaign_id bigint references public.ads_campaigns(id) on delete set null,
  notes           text,
  created_by      text,
  created_at      timestamptz not null default now(),
  updated_at      timestamptz not null default now()
);
create index if not exists idx_ads_experiments_status on public.ads_experiments(status);

create or replace view public.ads_experiment_results as
select
  e.id                as experiment_id,
  e.name              as experiment_name,
  e.platform,
  e.status,
  e.started_at,
  e.ended_at,
  e.split_pct,
  e.control_campaign_id,
  cc.name             as control_name,
  cp.spend            as control_spend,
  cp.leads            as control_leads,
  cp.clicks           as control_clicks,
  cp.impressions      as control_impressions,
  cp.cpl              as control_cpl,
  cp.ctr_pct          as control_ctr_pct,
  (select count(*)    from public.ads_campaign_performance v where v.campaign_id = ANY(e.variant_campaign_ids)) as variant_count,
  (select coalesce(sum(v.spend), 0)       from public.ads_campaign_performance v where v.campaign_id = ANY(e.variant_campaign_ids)) as variant_spend,
  (select coalesce(sum(v.leads), 0)       from public.ads_campaign_performance v where v.campaign_id = ANY(e.variant_campaign_ids)) as variant_leads,
  (select coalesce(sum(v.clicks), 0)      from public.ads_campaign_performance v where v.campaign_id = ANY(e.variant_campaign_ids)) as variant_clicks,
  (select coalesce(sum(v.impressions), 0) from public.ads_campaign_performance v where v.campaign_id = ANY(e.variant_campaign_ids)) as variant_impressions
from public.ads_experiments e
left join public.ads_campaigns cc on cc.id = e.control_campaign_id
left join public.ads_campaign_performance cp on cp.campaign_id = e.control_campaign_id;

alter table public.ads_experiments enable row level security;
do $$ begin
  if not exists (select 1 from pg_policies where schemaname='public' and tablename='ads_experiments' and policyname='ads_experiments_all') then
    create policy ads_experiments_all on public.ads_experiments for all using (true) with check (true);
  end if;
end $$;

