-- =====================================================================
-- Beithady v2 — Phase H: Ads module (port from VoltAuto)
-- =====================================================================
-- Mirrors the proven VoltAuto ads architecture (C:\Voltauto-pricing\
-- supabase\migrations\20260418_ads_module.sql) with Beithady-specific
-- extensions:
--   - campaigns gain building_codes[] (which BH-* the campaign promotes)
--   - ad_sets gain target_countries[] + target_interests[] (CRM Market
--     Intelligence drives this)
--   - ads gain gallery_asset_ids[] (Phase D's beithady_gallery_assets
--     marked ad_eligible feed the carousel creatives)
--   - leads gain building_interest + matched_reservation_id for the
--     ad-to-booking attribution trigger
--
-- Three views ported verbatim plus one Beithady extension
-- (ads_lead_funnel JOINs guesty_reservations for attribution status).

-- 1. Ad accounts — one row per (platform, external ad-account id)
create table if not exists public.ads_accounts (
  id                       bigint generated by default as identity primary key,
  platform                 text not null check (platform in ('meta','google','tiktok')),
  external_id              text not null,
  name                     text not null,
  currency                 text not null default 'USD',
  timezone                 text not null default 'Africa/Cairo',
  status                   text not null default 'active' check (status in ('active','paused','archived')),
  meta_business_id         text,
  meta_app_id              text,
  fb_page_id               text,                                  -- Facebook page used for CTWA + creatives
  google_login_customer_id text,
  notes                    text default '',
  created_at               timestamptz not null default now(),
  updated_at               timestamptz not null default now(),
  unique (platform, external_id)
);

-- 2. Campaigns
