create or replace view public.ads_campaign_performance as
select
  c.id as campaign_id, c.name as campaign_name, c.platform,
  a.name as account_name, a.currency as account_currency,
  c.status as campaign_status, c.objective,
  c.building_codes,
  coalesce(sum(m.impressions),0) as impressions,
  coalesce(sum(m.clicks),0)      as clicks,
  coalesce(sum(m.spend_micros),0)/1000000.0 as spend,
  coalesce(sum(m.leads),0)       as leads,
  coalesce(sum(m.conversions),0) as conversions,
  case when sum(m.clicks)>0  then (sum(m.spend_micros)/1000000.0)/nullif(sum(m.clicks),0) end as cpc,
  case when sum(m.leads)>0   then (sum(m.spend_micros)/1000000.0)/nullif(sum(m.leads),0)  end as cpl,
  case when sum(m.impressions)>0 then 100.0*sum(m.clicks)::numeric/nullif(sum(m.impressions),0) end as ctr_pct,
  min(m.metric_date) as first_date, max(m.metric_date) as last_date
from public.ads_campaigns c
join public.ads_accounts a on a.id=c.account_id
left join public.ads_daily_metrics m on m.campaign_id=c.id and m.ad_id is null and m.ad_set_id is null
group by c.id, c.name, c.platform, a.name, a.currency, c.status, c.objective, c.building_codes;

create or replace view public.ads_overview_daily as
select m.metric_date, m.platform,
  sum(m.impressions) as impressions,
  sum(m.clicks)      as clicks,
  sum(m.spend_micros)/1000000.0 as spend,
  sum(m.leads)       as leads,
  case when sum(m.leads)>0 then (sum(m.spend_micros)/1000000.0)/nullif(sum(m.leads),0) end as cpl
from public.ads_daily_metrics m
where m.ad_id is null and m.ad_set_id is null
group by m.metric_date, m.platform
order by m.metric_date desc;

create or replace view public.ads_lead_funnel as
select
  l.id as lead_id,
  l.created_at,
  l.platform,
  l.campaign_id,
  cp.name as campaign_name,
  cp.building_codes,
  l.form_name,
  l.full_name,
  l.phone_e164,
  l.email,
  l.country,
  l.building_interest,
  l.beithady_guest_id,
  l.matched_reservation_id,
  l.matched_at,
  -- Booking attribution status
  case
    when l.matched_reservation_id is not null then 'booked'
    when l.processed_at is not null then 'processed'
    else 'new'
  end as funnel_stage,
  r.host_payout as booking_value,
  r.currency as booking_currency,
  r.check_in_date as booking_check_in
from public.ads_leads l
left join public.ads_campaigns cp on cp.id = l.campaign_id
left join public.guesty_reservations r on r.id = l.matched_reservation_id
order by l.created_at desc;

-- =====================================================================
-- 11. Attribution trigger — when a guesty_reservation lands and the
-- guest's phone matches an ads_lead from the past 90 days, link them.
-- =====================================================================

create or replace function public.beithady_ads_match_lead_to_reservation()
returns trigger language plpgsql as $$
declare
  guest_phone_e164 text;
begin
  if new.guest_phone is null then return new; end if;
  guest_phone_e164 := '+' || regexp_replace(coalesce(new.guest_phone, ''), '[^0-9]', '', 'g');
  if length(guest_phone_e164) < 10 then return new; end if;

  update public.ads_leads
     set matched_reservation_id = new.id,
         matched_at             = now()
   where phone_e164 = guest_phone_e164
     and matched_reservation_id is null
     and created_at > now() - interval '90 days'
     and (new.created_at_odoo is null or new.created_at_odoo > created_at);

  return new;
end $$;

drop trigger if exists ads_attribution_on_reservation on public.guesty_reservations;
create trigger ads_attribution_on_reservation
  after insert or update on public.guesty_reservations
  for each row execute function public.beithady_ads_match_lead_to_reservation();

